package com.pramila;

public class Type_casting {
    public static void main(String[] args) {
        // Declaring an Integer datatype
        int a = 3;

        // Casting to Large datatype
        double db = (double)a;

        // Print and display the casted value
        System.out.println("casted with double Datatype"+db);

        // Narrow Casting conversion
        int db1 = (int)db;

        // Print an display narrow casted value
        System.out.println("Casted with integer datatype"+db1);
    }
}
